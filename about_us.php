        <p>LPU SAEINDIA COLLEGIATE CLUB (LSCC) is a student-organized club of Lovely Professional University Jalandhar (Punjab) which deals with the mechanical engineering projects. LSCC is affiliated to Society of Automotive Engineers India chapter, an International organization which gives National and International exposure to the engineering students. The main motive behind this is to get each and every college to indulge in such kind of activities which provides a competitive environment to the students and also inspire them to come up with new and innovative ideas. From the last 6 years, LSCC is organizing this Go-kart racing event in the first year it was given the name as National Go-Kart championship 2012-13 which was a great success with 26 teams in Dynamic Phase. In season II this event was organized by name of International Go-kart Championship 2013-14 (IGC 2013-14) as it was taken to international standards in which 120 teams participated from all over India and world. In the year (2014-15) 110 teams registered out which total 70 teams qualified for the final round. In the year (2015-16), 130+ teams registered for the event and 90 teams qualified for the final round from across the globe. In the year (2016-17), 150+ teams registered for the event and 100 teams qualified for the final event across India. Total 27 vehicles were selected for final Race. In the Year 2017-18 90+teams Registered for the event and 44 teams qualified for the final event across India. Finally, 26 teams were selected for the final endurance race. This is the Seventh time LSCC is organizing this event in which National and International participants have to manufacture a full functioning Go-Kart.</p>
